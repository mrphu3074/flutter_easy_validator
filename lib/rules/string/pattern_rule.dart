class PatternRule extends Rule {
  PatternRule(this.regex, {message})
      : super(message: message ?? getStringMessage('pattern'));

  final RegExp regex;

  @override
  Map get params => {'path': path, 'regex': regex};

  @override
  bool test(value, {Map params}) {
    return isAbsent(value) || (value is String && regex.hasMatch(value));
  }
}
